package com.example.demo;

public class Par {
	String p ;
	String color;
	public Par() {
	}
	public Par(String p,String color) {
		this.p = p;
		this.color = color;
	}
	public String getPar() {
		return p;
	}
	public void setPar(String par) {
		this.p = par;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
}
